type SubC = [string, string[]]
type MidC = [string, SubC[]]
type BigC = [string, MidC[]]

function build(id: string, grade: string, label: string, data: BigC[]): Curriculum {
  return {
    id, grade, label,
    units: data.map((u, ui) => ({
      id: `${id}-u${ui}`, name: u[0],
      mids: u[1].map((m, mi) => ({
        id: `${id}-u${ui}m${mi}`, name: m[0],
        subs: m[1].map((s, si) => ({
          id: `${id}-u${ui}m${mi}s${si}`, name: s[0],
          types: s[1].map((t, ti) => ({ id: `${id}-u${ui}m${mi}s${si}t${ti}`, name: t })),
        })),
      })),
    })),
  }
}

