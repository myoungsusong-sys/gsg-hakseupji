// 기출 회차 학년(고1/고2/고3) → 기본 과정 id (태깅 시 초기 선택)
export function defaultCurriculumForGrade(grade: string): string {
  if (grade === '고1') return 'h-cm1'
  if (grade === '고2') return 'h-alg'
  if (grade === '고3') return 'h-calc2'
  return 'm1-1'
}

function eachType(): { t: TypeNode; s: SubUnit; m: MidUnit; u: BigUnit; c: Curriculum }[] {
  const out: { t: TypeNode; s: SubUnit; m: MidUnit; u: BigUnit; c: Curriculum }[] = []
  for (const c of CURRICULA)
    for (const u of c.units)
      for (const m of u.mids)
        for (const s of m.subs)
          for (const t of s.types) out.push({ t, s, m, u, c })
  return out
}

export function allTypeIds(): string[] {
  return eachType().map(x => x.t.id)
}

export function typeName(typeId: string): string {
  return eachType().find(x => x.t.id === typeId)?.t.name ?? typeId
}

// "대단원 · 중단원" (범위 요약용)
export function typeUnitName(typeId: string): string {
  const x = eachType().find(y => y.t.id === typeId)
  return x ? `${x.u.name} · ${x.m.name}` : ''
}

// 유형이 속한 소단원 id (개념 매칭용)
export function typeSubUnitId(typeId: string): string {
  return eachType().find(x => x.t.id === typeId)?.s.id ?? ''
}
